// Package avastha implements Planetary Avasthas (states) from Brihat Parashara
// Hora Shastra (BPHS). Avasthas describe the condition or mood of a planet
// based on its sign placement and relationships with other planets.
//
// Six Shayana (residence) Avasthas are supported:
//   - Lajjita (ashamed): planet in enemy sign or debilitation
//   - Garvita (proud): planet in own sign or exaltation
//   - Kshudhita (hungry): planet in enemy sign aspected by malefic
//   - Trishita (thirsty): planet in water sign aspected by malefic without benefic aspect
//   - Mudita (happy): planet in friend's sign with benefic aspect
//   - Kshobhita (agitated): planet conjunct Sun and aspected by malefic
package avastha
